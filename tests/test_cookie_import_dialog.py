def test_file_import_writes_with_permissions(tmp_path, monkeypatch, qtbot):
    """Choosing a valid cookie file and clicking Import writes with 0o600 perms."""
    monkeypatch.setattr(paths, "_root_override", str(tmp_path))

    # Create a valid source cookie file
    source = tmp_path / "my_cookies.txt"
    cookie_text = ".youtube.com\tFALSE\t/\tFALSE\t0\tVISITOR\tval"
    source.write_text(cookie_text)

    toasts = []
    from musicstreamer.ui_qt.cookie_import_dialog import CookieImportDialog
    dlg = CookieImportDialog(toast_callback=toasts.append)
    qtbot.addWidget(dlg)

    # Simulate the user picking a file (bypass QFileDialog)
    dlg._selected_file_path = str(source)
    dlg._file_label.setText(source.name[:40])
    dlg._file_import_btn.setVisible(True)

    dlg._on_file_import()

    cookie_file = tmp_path / "cookies.txt"
    assert cookie_file.exists()
    assert cookie_file.read_text() == cookie_text
    file_mode = stat.S_IMODE(os.stat(str(cookie_file)).st_mode)
    assert file_mode == 0o600
    assert "YouTube cookies imported." in toasts


