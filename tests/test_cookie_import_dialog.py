def test_google_login_launches_qprocess(qtbot, monkeypatch):
    """Clicking 'Open Google Login' starts QProcess with oauth_helper --mode google."""
    from musicstreamer.ui_qt.cookie_import_dialog import CookieImportDialog

    mock_process = MagicMock()
    with patch("musicstreamer.ui_qt.cookie_import_dialog.QProcess", return_value=mock_process):
        dlg = CookieImportDialog(toast_callback=lambda msg: None)
        qtbot.addWidget(dlg)
        dlg._on_google_login()

    mock_process.start.assert_called_once()
    # Verify sys.executable used as program (not a shell call)
    start_args = mock_process.start.call_args
    import sys
    assert start_args[0][0] == sys.executable
    assert "--mode" in start_args[0][1]
    assert "google" in start_args[0][1]


